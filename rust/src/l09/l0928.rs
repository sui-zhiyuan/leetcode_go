use std::{collections::HashSet, ops::Add};

use crate::common::DisjointSet;

impl Solution {
    pub fn min_malware_spread(graph: Vec<Vec<i32>>, initial: Vec<i32>) -> i32 {
        let initial = initial
            .into_iter()
            .map(|v| usize::try_from(v).unwrap())
            .collect::<Vec<_>>();
        let n = graph.len();
        let mut effected = vec![0; n];
        for i in initial.iter() {
            effected[*i] = 1;
        }

        let initial_set = initial.iter().collect::<HashSet<_>>();

        let mut dsu = DisjointSet::new(&effected, <i32 as Add>::add);
        for (i, row) in graph.iter().enumerate() {
            for (j, &cell) in row.iter().enumerate().take(i) {
                if initial_set.contains(&i) || initial_set.contains(&j) {
                    continue;
                }
                if cell == 1 {
                    dsu.union(i, j);
                }
            }
        }
        let original_dsu = dsu;

        let calculate_effect = |skip: usize| {
            // dbg!(("skip", skip));
            let mut dsu = original_dsu.clone();
            for &i in initial.iter() {
                if i == skip {
                    continue;
                }
                for (j, &cell) in graph[i].iter().enumerate() {
                    if cell == 1 {
                        dsu.union(i, j);
                    }
                }
            }

            let mut visited = HashSet::<usize>::new();
            let mut effected = 0;
            for &i in initial.iter() {
                let root = dsu.find(i);
                // dbg!(("find" , i , root));
                if visited.insert(root) {
                    // dbg!(dsu.size(root));
                    effected += dsu.size(root);
                }
            }
            (dsu, effected)
        };

        let (mut dsu, all_effected) = calculate_effect(usize::MAX);

        let mut effect = 0;
        let mut result = initial[0];
        for &i in initial.iter() {
            let size = if dsu.value(i) == 1 {
                // dbg!(("single_source", i , dsu.size(i)));
                dsu.size(i) -1 
            } else {
                let calc1 = calculate_effect(i);
                // dbg!(("multiple_source", i , all_effected , calc1.1));
                all_effected - calc1.1
            };

            if size > effect || (size == effect && i < result){
                effect = size;
                result = i;
            }
        }

        result.try_into().unwrap()
    }
}

